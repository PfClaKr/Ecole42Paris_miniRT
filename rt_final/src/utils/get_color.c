#include "utils.h"

int	get_color(t_vec3 pixel)
{
	int	x;
	int	y;
	int	z;

	x = ft_clamp(pixel.x) * 255;
	y = ft_clamp(pixel.y) * 255;
	z = ft_clamp(pixel.z) * 255;
	return (x << 16 | y << 8 | z);
}
